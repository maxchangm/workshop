# TODO 16 (‚òÖ‚òÖ‚òÖ): Reciprocal Rank Fusion (RRF)
# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
# Combine the two ranked lists into one using the RRF formula:
#     score(d) = Œ£  1 / (RRF_K + rank_i)
# where rank_i is the 1-based position in each list.
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

def _hybrid_search(
    conn: duckdb.DuckDBPyConnection, query: str, top_k: int = 5,
) -> list[dict]:
    """Run vector + BM25 search, fuse with RRF, return top_k results."""
    print(f"\nüîç Searching: '{query}'")

    # Embed the query once (reused by vector search)
    query_vec = ollama.embed(model=EMBED_MODEL, input=query)["embeddings"][0]

    # Retrieve more candidates than top_k so RRF has room to re-rank.
    vec_results = _search_vector(conn, query_vec, limit=top_k * 2)
    bm25_results = _search_bm25(conn, query, limit=top_k * 2)

    # ‚îå‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îê
    # ‚îÇ  TODO 16: Implement RRF.                                     ‚îÇ
    # ‚îÇ                                                              ‚îÇ
    # ‚îÇ  fused_scores: dict[int, float] = {}                         ‚îÇ
    # ‚îÇ                                                              ‚îÇ
    # ‚îÇ  For each ranked list (vec_results, bm25_results):           ‚îÇ
    # ‚îÇ    for i, (chunk_id, _score) in enumerate(results):          ‚îÇ
    # ‚îÇ        rank = i + 1                                          ‚îÇ
    # ‚îÇ        fused_scores[chunk_id] = (                            ‚îÇ
    # ‚îÇ            fused_scores.get(chunk_id, 0.0)                   ‚îÇ
    # ‚îÇ            + 1.0 / (RRF_K + rank)                            ‚îÇ
    # ‚îÇ        )                                                     ‚îÇ
    # ‚îÇ                                                              ‚îÇ
    # ‚îÇ  Then sort by fused score descending and take top_k IDs.     ‚îÇ
    # ‚îÇ  Fetch their text from the DB:                               ‚îÇ
    # ‚îÇ    conn.execute(                                             ‚îÇ
    # ‚îÇ        "SELECT chunk_id, text FROM rag_chunks "              ‚îÇ
    # ‚îÇ        "WHERE chunk_id = ?", [cid]                           ‚îÇ
    # ‚îÇ    ).fetchone()                                              ‚îÇ
    # ‚îÇ                                                              ‚îÇ
    # ‚îÇ  Return a list of dicts:                                     ‚îÇ
    # ‚îÇ    [{"chunk_id": ‚Ä¶, "text": ‚Ä¶, "rrf_score": ‚Ä¶}, ‚Ä¶]          ‚îÇ
    # ‚îî‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îò
    raise NotImplementedError("TODO 16: RRF fusion")


